const LOGO_TAGS = LOGO_DS

// Build a mask SVG on a large canvas. White fills everything; the logo
// (in black, centered) is subtracted via a luminance mask so it becomes
// a transparent hole. Canvas 1600x1600, logo centered with translate.
const maskSvg = `<svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 1600 1600"><defs><mask id="m"><rect width="1600" height="1600" fill="#fff"/><g transform="translate(550,580)" fill="#000">${LOGO_TAGS}</g></mask></defs><rect width="1600" height="1600" fill="#fff" mask="url(#m)"/></svg>`
const maskUrl = `url("data:image/svg+xml,${encodeURIComponent(maskSvg)}")`

export default function AboutReveal() {
  const sectionRef = useRef<HTMLElement>(null)
  const maskRef = useRef<HTMLDivElement>(null)
  const contentRef = useRef<HTMLDivElement>(null)
  const scrimRef = useRef<HTMLDivElement>(null)
  const videoRef = useRef<HTMLVideoElement>(null)

  useEffect(() => {
    const ctx = gsap.context(() => {
      // Lock the initial state so any scroll-back returns here.
      gsap.set(maskRef.current, { scale: 1 })
      gsap.set(scrimRef.current, { opacity: 0 })
      gsap.set(contentRef.current, { opacity: 0, y: 30 })

      const tl = gsap.timeline({
        scrollTrigger: {
          trigger: sectionRef.current,
          start: 'top top',
          end: '+=200%',
          scrub: 1,
          pin: true,
          anticipatePin: 1,
          invalidateOnRefresh: true,
        },
      })

      // Phase 1: scale the mask up so the logo-hole grows until it covers
      // the whole viewport — the white clears purely by zooming (no fade).
      // Finishes at 0.6 so the white is fully gone before text appears.
      tl.fromTo(
        maskRef.current,
        { scale: 1 },
        { scale: 260, ease: 'power1.in', duration: 0.6 },
        0
      )

      // Scrim fades in once the video is fully revealed
      tl.fromTo(
        scrimRef.current,
        { opacity: 0 },
        { opacity: 1, ease: 'none', duration: 0.2 },
        0.6
      )

      // Content fades in after the reveal is complete
      tl.fromTo(
        contentRef.current,
        { opacity: 0, y: 30 },
        { opacity: 1, y: 0, ease: 'none', duration: 0.3 },
        0.7
      )
    }, sectionRef)

    return () => ctx.revert()
  }, [])

  return (
    <section ref={sectionRef} className={styles.section}>
      {/* Video background */}
      <video
        ref={videoRef}
        className={styles.video}
        autoPlay
        muted
        loop
        playsInline
        preload="auto"
      >
        <source src="/videos/abouthome.mp4" type="video/mp4" />
      </video>

      {/* Dark scrim for text readability */}
      <div ref={scrimRef} className={styles.scrim} />

      {/* White overlay with logo-shaped hole */}
      <div
        ref={maskRef}
        className={styles.maskLayer}
        style={{ ['--mask-url' as string]: maskUrl }}
      />

      {/* Mission / Reputation content */}
      <div ref={contentRef} className={styles.content}>
        <div className={styles.blocks}>
          <div className={styles.block}>
            <h2 className={styles.blockTitle}>Our Mission</h2>
            <p className={styles.blockText}>
              Helping businesses grow through data-driven marketing, creative
              storytelling, and results-focused strategy.
            </p>
          </div>
          <div className={styles.block}>
            <h2 className={styles.blockTitle}>Our Reputation</h2>
            <p className={styles.blockText}>
              Trusted by brands across industries to deliver impactful campaigns
              across TV, radio, digital, and social platforms.
            </p>
          </div>
        </div>

        <div className={styles.buttons} style={{ pointerEvents: 'auto' }}>
          <SplashButton color="neutral" size="md" href="/contact">
            Contact Us
          </SplashButton>
          <SplashButton color="blue" size="md" href="/about">
            About Splash
          </SplashButton>
        </div>
      </div>
    </section>
  )
}